#include <iostream>
#include <string>
using namespace std;



int main()
{
	string s;
	cout << "여러 줄의 문자열을 입력하세요. 입력의 끝은 &문자입니다. " << endl;
	getline(cin, s, '&'); //문자열 입력
	cin.ignore(); //문자 한개만큼 입력버퍼를 비움

	string f, r;
	cout << endl << "find ";
	getline(cin, f, '\n');
	cout << "replace ";
	getline(cin, r, '\n');

	int startindex = 0;
	while (true) {
		int findex = s.find(f, startindex);
		if (findex == -1)
			break;
		s.replace(findex, f.length(), r); //findex부터 문자열 f의 길이만큼 문자열 r로 변경
		startindex = findex + r.length();
	}

	cout << s << endl;


	return 0;
}